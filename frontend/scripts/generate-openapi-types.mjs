import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { execFileSync } from "node:child_process";

const repoRoot = path.resolve(process.cwd(), "..");
const specPath = path.resolve(repoRoot, "backend", "openapi.yaml");
const outPath = path.resolve(process.cwd(), "src", "lib", "openapi-types.ts");

if (!fs.existsSync(specPath)) {
  console.error(
    `Missing OpenAPI spec at ${specPath}. Run: python backend/manage.py spectacular --file backend/openapi.yaml`,
  );
  process.exit(1);
}

fs.mkdirSync(path.dirname(outPath), { recursive: true });

// Use the official CLI to avoid API version quirks.
execFileSync(
  process.execPath,
  [path.resolve("node_modules/.bin/openapi-typescript"), specPath, "-o", outPath],
  { stdio: "inherit" },
);

// Ensure a stable header exists (handy for reviews).
const generated = fs.readFileSync(outPath, "utf-8");
if (!generated.includes("This file is generated")) {
  fs.writeFileSync(
    outPath,
    `/* eslint-disable */\n/* This file is generated by scripts/generate-openapi-types.mjs */\n\n${generated}`,
    "utf-8",
  );
}

console.log(`Wrote ${outPath}`);

